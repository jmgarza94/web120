            <h2 class="pageID">Galleries</h2>
                <p>Galleries in HTML and JavaScript are a great way to display a variety of images, and can make a website look much more professional. It is a very valuable tool for businesses or artists that should not be overlooked. There are different ways to go about creating a gallery and different ways it can be implemented.</p>
                <p>There are different types of galleries you can use in an html or php document. One of the most common is an automatic sliding gallery. These are best created with lightbox and there are plenty of examples that people can use online that won’t require the developer to write the code themselves; just perhaps tweak it to fit the needs of the client. This can be used in a banner above the Nav, or below to showcase an artist’s work or different images that help show the audience the type of thing they can expect from the client.</p>
                <p>Another common type of gallery is a sliding gallery that must be clicked on at a certain area to progress to the next slide. This is usually initiated by first clicking on an image on the page. It is also common practice to make the images displayed on the webpage responsive. On a desktop it could show maybe 4 images side by side, then maybe only 2 on a tablet and then 1 image per row when viewed on a mobile device. When the image is clicked it then pops up to a larger size and that’s when the slider appears. It can be cycled through on a desktop computer by using the arrow keys or clicking the arrows, or on a mobile device by swiping left or right on the image. This allows the client to showcase whatever series of images they like to a wider audience.</p>
                <p>Many galleries are written in a mix of HTML, CSS and JavaScript, however it is possible to create simpler galleries that are still responsive with just HTML and some more advanced CSS. As previously stated, galleries can add a lot to a website, like making it look more professional, creating an effective way for clients to showcase images of their business, and still remaining responsive to bring in a larger audience.</p>
